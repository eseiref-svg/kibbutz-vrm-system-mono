// Helper to determine category from name
function getCategoryForSupplier(name) {
    const n = name.toLowerCase();
    if (n.includes('מזון') || n.includes('פירות') || n.includes('לחם') || n.includes('קייטרינג') || n.includes('שוק')) return categories[0]; // Food
    if (n.includes('חקלאות') || n.includes('זרעים') || n.includes('גינון') || n.includes('נוי') || n.includes('משתל')) return categories[1]; // Agriculture
    if (n.includes('חשמל') || n.includes('מיזוג') || n.includes('איטום') || n.includes('בניין') || n.includes('שיפוץ')) return categories[2]; // Infrastructure
    if (n.includes('הובלות') || n.includes('מוסך') || n.includes('רכב') || n.includes('צמיג') || n.includes('מונית')) return categories[3]; // Transport
    if (n.includes('מחשב') || n.includes('טכנולוג') || n.includes('מערכות') || n.includes('דיגיטלי')) return categories[4]; // Tech
    if (n.includes('הפקות') || n.includes('מוסיק') || n.includes('תרבות') || n.includes('טיול') || n.includes('אירוע')) return categories[5]; // Culture
    if (n.includes('משרד') || n.includes('דפוס') || n.includes('נכסי') || n.includes('ציוד')) return categories[6]; // Office
    if (n.includes('רפוא') || n.includes('בריאות') || n.includes('פארם') || n.includes('ספורט')) return categories[7]; // Health
    if (n.includes('בטיחות') || n.includes('שמירה') || n.includes('אבטחה')) return categories[8]; // Safety
    if (n.includes('עו"ד') || n.includes('יועץ') || n.includes('שמאי') || n.includes('ביטוח')) return categories[9]; // Professional

    // Default random
    return categories[Math.floor(Math.random() * categories.length)];
}

