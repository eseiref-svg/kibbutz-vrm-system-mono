// Simple transliteration map for emails
const hebrewToEnglish = {
    'א': 'a', 'ב': 'b', 'ג': 'g', 'ד': 'd', 'ה': 'h', 'ו': 'v', 'ז': 'z', 'ח': 'ch', 'ט': 't', 'י': 'y', 'כ': 'k', 'ל': 'l', 'מ': 'm', 'נ': 'n', 'ס': 's', 'ע': 'a', 'פ': 'p', 'צ': 'tz', 'ק': 'k', 'ר': 'r', 'ש': 'sh', 'ת': 't',
    'ן': 'n', 'ם': 'm', 'ך': 'ch', 'ף': 'f', 'ץ': 'tz'
};

function transliterate(str) {
    return str.split('').map(char => hebrewToEnglish[char] || char).join('').toLowerCase();
}

function getRandomElement(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

function generateBN(index) {
    const base = 510000000;
    return (base + index).toString();
}

function generatePhone() {
    return '0' + (500000000 + Math.floor(Math.random() * 99999999)).toString();
}

async function seedSuppliers() {
    const client = await pool.connect();

    try {
        console.log('🌱 Starting Supplier Seeding...');
        await client.query('BEGIN');

        // 1. Create Categories
        console.log('Inserting Supplier Fields...');
        const fieldMap = new Map(); // Name -> ID

        for (const cat of categories) {
            // Check if exists
            const existing = await client.query('SELECT supplier_field_id FROM supplier_field WHERE field = $1', [cat.field]);
            let fieldId;

            if (existing.rows.length > 0) {
                fieldId = existing.rows[0].supplier_field_id;
            } else {
                const res = await client.query(`
                    INSERT INTO supplier_field (field, tags)
                    VALUES ($1, $2)
                    RETURNING supplier_field_id
                `, [cat.field, cat.tags]);
                fieldId = res.rows[0].supplier_field_id;
            }
            fieldMap.set(cat.field, fieldId);
        }

        // 2. Process Suppliers
        const names = supplierNamesRaw.split('\n').map(s => s.trim()).filter(s => s);
        console.log(`Processing ${names.length} suppliers...`);

        for (let i = 0; i < names.length; i++) {
            const name = names[i];
            const category = getCategoryForSupplier(name);
            const fieldId = fieldMap.get(category.field);
            const address = {
                street: getRandomElement(streets),
                city: getRandomElement(cities),
                num: Math.floor(Math.random() * 100) + 1,
                zip: Math.floor(Math.random() * 90000) + 10000
            };

            // Insert Address
            const addrRes = await client.query(`
                INSERT INTO address (city, street_name, house_no, zip_code, phone_no, additional)
                VALUES ($1, $2, $3, $4, $5, '')
                RETURNING address_id
            `, [address.city, address.street, address.num.toString(), address.zip.toString(), generatePhone()]);
            const addressId = addrRes.rows[0].address_id;

            // Generate POC details
            const pocFirstName = getRandomElement(firstNames);
            const pocSurname = getRandomElement(surnames);
            const pocName = `${pocFirstName} ${pocSurname}`;

            // Generate clean email
            const cleanEnPoc = transliterate(pocFirstName) + '.' + transliterate(pocSurname);
            // Some supplier names are long/complex, let's just use "company" + index or simplified identifier
            // Or try to transliterate the first word of the supplier
            const firstWordSupplier = name.split(' ')[0].replace(/["']/g, ''); // Remove quotes
            const cleanEnSupplier = transliterate(firstWordSupplier).replace(/[^a-z0-9]/g, '');
            const safeSupplier = cleanEnSupplier.length > 2 ? cleanEnSupplier : `supplier${i}`;

            const email = `${cleanEnPoc}@${safeSupplier}.co.il`;

            await client.query(`
                INSERT INTO supplier (
                    name, status, payment_terms,
                    poc_name, poc_email, poc_phone,
                    supplier_field_id, address_id, registry_date, is_active
                )
                VALUES ($1, $2, $3, $4, $5, $6, $7, $8, NOW(), true)
            `, [
                name,
                'approved', // Status
                `שוטף+${Math.random() > 0.5 ? 30 : 60}`, // Terms
                pocName,
                email,
                generatePhone(),
                fieldId,
                addressId
            ]);
        }

        await client.query('COMMIT');
        console.log('✅ Suppliers seeded successfully!');

    } catch (err) {
        await client.query('ROLLBACK');
        console.error('❌ Error seeding suppliers:', err);
        process.exit(1);
    } finally {
        client.release();
        await pool.end();
    }
}

seedSuppliers();
